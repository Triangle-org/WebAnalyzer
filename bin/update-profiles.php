function deviceString($s)
{
    if (is_null($s) || $s == '') {
        return 'null';
    }

    return "'" . addslashes(trim($s)) . "'";
}

function deviceType($type)
{
    switch ($type) {
        case 'mobile':
            return 'DeviceType::MOBILE';
        case 'tablet':
            return 'DeviceType::TABLET';
    }
}